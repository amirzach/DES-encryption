def hex_to_binary(hex_string):
    binary_string = bin(int(hex_string, 16))[2:]
    return binary_string.zfill(len(hex_string) * 4)

def binary_to_hex(binary_string):
    hex_string = hex(int(binary_string, 2))[2:].upper()
    return hex_string.zfill(len(binary_string) // 4)

def permute(input_bits, table):
    return ''.join(input_bits[i - 1] for i in table)

def xor(a, b):
    return ''.join('0' if i == j else '1' for i, j in zip(a, b))

def sbox_substitution(input_bits):
    output_bits = ""
    for i in range(8):
        block = input_bits[i * 6:(i + 1) * 6]
        row = int(block[0] + block[-1], 2)
        col = int(block[1:5], 2)
        output_bits += bin(SBOX[i][row][col])[2:].zfill(4)
    return output_bits

def feistel_function(right, subkey):
    print("  Feistel Function:")
    expanded_right = permute(right, E)
    print("   - Expanded Right:", binary_to_hex(expanded_right))
    
    xored = xor(expanded_right, subkey)
    print("   - XOR with Subkey:", binary_to_hex(xored))
    
    substituted = sbox_substitution(xored)
    print("   - After S-Box Substitution:", binary_to_hex(substituted))
    
    permuted = permute(substituted, P)
    print("   - After Permutation (P):", binary_to_hex(permuted))
    return permuted

def des_encrypt(plaintext_hex, key_hex):
    plaintext_binary = hex_to_binary(plaintext_hex)
    key_binary = hex_to_binary(key_hex)
    
    ip_result = permute(plaintext_binary, IP)
    print("Initial Permutation (IP):", binary_to_hex(ip_result))
    
    left, right = ip_result[:32], ip_result[32:]
    print("L0:", binary_to_hex(left))
    print("R0:", binary_to_hex(right))
    
    # Simplified key schedule
    subkeys = [key_binary[i:i + 48] for i in range(0, 16)]
    
    for i in range(16):
        print(f"\nRound {i + 1}:")
        print("Subkey:", binary_to_hex(subkeys[i]))
        temp_right = feistel_function(right, subkeys[i])
        new_left = xor(left, temp_right)
        
        print(f"  L{i + 1} (new):", binary_to_hex(right))
        print(f"  R{i + 1} (new):", binary_to_hex(new_left))
        
        left, right = right, new_left
    
    combined = right + left
    fp_result = permute(combined, FP)
    print("\nFinal Permutation (FP):", binary_to_hex(fp_result))
    print("Ciphertext:", binary_to_hex(fp_result))

